package com.github.nallafy;

public class SwitchCase {

    public static void main(String[] args) {
        short choice = 3;
        switch (choice) {
            case 1:
                System.out.println("Value is 1");
                break;
            case 2:
                System.out.println("Value is 2");
                break;
            default:
                System.out.println("Value is invalid");
        }


    }
}

